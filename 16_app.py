# estructura de bucle For
# Sintaxis
# for value in sequence:
#   instrucci√≥n, que es lo que debe hacer mientras se cumple el bucle
# Ejemplo
# Listado de frutas
frutas = ["manzana", "mandarina", "arandanos", "fresas", "maracuya"]
for fruta in frutas:
    print(fruta)

